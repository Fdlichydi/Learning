    <!-- Toggle Script -->
    <script>
        const toggleBtn = document.getElementById('chatbot-toggle');
        const chatbotBox = document.getElementById('chatbot-container');

        toggleBtn.addEventListener('click', function() {
            chatbotBox.style.display = chatbotBox.style.display === 'none' ? 'block' : 'none';
        });

        // Chat submit
        document.getElementById('chatbot-form').addEventListener('submit', function(e) {
            e.preventDefault();
            let input = document.getElementById('chat-input');
            let message = input.value;
            let chatbox = document.getElementById('chatbox');

            if (message.trim() !== '') {
                chatbox.innerHTML += '<div><strong>Anda:</strong> ' + message + '</div>';
                fetch("{{ route('chatbot.respond') }}", {
                        method: 'POST',
                        headers: {
                            'Content-Type': 'application/json',
                            'X-CSRF-TOKEN': "{{ csrf_token() }}"
                        },
                        body: JSON.stringify({
                            message: message
                        })
                    })
                    .then(res => res.json())
                    .then(data => {
                        chatbox.innerHTML += '<div><strong>Bot:</strong> ' + data.response + '</div>';
                        chatbox.scrollTop = chatbox.scrollHeight;
                    });
                input.value = '';
            }
        });
    </script>


